通常のhtmlファイルをリクエストする手順

1, ブラウザのロケーションバーにアドレスを入力する（html）
2, ブラウザはアドレスのコンピュータにインターネットを介してファイルをリクエストするメッセージを送る。
3, Apache HTTPサーバがメッセージを受け取り、ディスク装置からファイルを読み込む。
4, Apacheがインターネットを介してそのファイルの内容をブラウザからのリクエストへのレスポンスとしてユーザのコンピュータに送り返す。
5, ブラウザはそのページに含まれるHTMLタグの指示に従って画面にページを表示する。

通常のPHPファイルをリクエストする手順（PHPが有効になっている通信の場合）

1, ブラウザのロケーションバーにアドレスを入力する（php）
2, ブラウザはアドレスのコンピュータにインターネットを介してphpファイルをリクエストするメッセージを送る。
3, Apache HTTPサーバがメッセージを受け取り、PHPエンジンにリクエストするメッセージを送る
4, PHPエンジンがphpファイルを読み込む
5, PHPエンジンがphpファイルの中のコマンドを実行し、データベースとデータの交換をする
6, PHPエンジンはプログラムの出力をApache HTTPサーバに返す
7, Apache HTTPサーバは、PHPエンジンから得たページの内容をブラウザからのリクエストへのレスポンスとしてインターネットを介してユーザのコンピュータに返す
8, ブラウザはそのページに含まれるHTMLタグの指示に従って画面にページを表示する

PHPはWebサーバで実行されることからサーバサイド言語と呼ばれる
JavaScriptなどの言語はWebブラウザに埋め込まれ、PCで動作してブラウザで処理を行えるため、クライアントサイド言語と呼ばれる

PHPの優れている点
・PHPはフリー（無料）
・PHPはフリー（自由）
PHPはオープンソースプロジェクトなので、その内部を誰でも調べることができる。
（PHPエンジンはC言語で書かれている）
・PHPはクロスプラットフォーム
Windows,Mac,Linux,その他多くのバージョンのUnixが動作するWebサーバーコンピューターでPHPを利用することができる。
さらに、Webサーバのオペレーティングシステムを切り替えても、通常はPHPプログラムを変更する必要はない。
PHPと一緒に使うWebサーバで一番人気のあるものは、Apacheですが、nginx、Microsoft Internet InformationServer、その他でもCGI規格をサポートするWebサーバであれば使用することができる。
PHPは、MySQL、PostgreSQL、Oracle、Microsoft SQL Server、SQLite、Redis、MongDBなどの多くのデータベースとも連携できる。
・PHPは幅広く使われている
2億以上のさまざまなWebサイトで使われている。
無数の小規模な個人のホームページからFacebookやWikipediaなどの巨大サイトにまで及びます。
・PHPは複雑さを隠す
数百万人の顧客の注文を処理するeコマース用の強力なエンジンをPHPで構築できる。
・PHPはWebプログラミングのために開発されている
他のほとんどのプログラミング言語と違い、PHPはWebページを生成するためにゼロから創り出された。
つまり、フォーム送信へのアクセスやデータベースとのやり取りなどの一般的なWebプログラミングタスクは、PHPの方が簡単なことが多い。
